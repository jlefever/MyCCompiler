int main()
{
    int a = 5, b = 9;

    printf("a >> 1 is %d\n", a >> 1);
    printf("b >> 1 is %d\n", b >> 1);
    printf("a << 1 is %d\n", a << 1);
    printf("b << 1 is %d\n", b << 1);
    printf("a >> b is %d\n", a >> b);
    printf("b >> a is %d\n", b >> a);
    printf("a << b is %d\n", a << b);
    printf("b << a is %d\n", b << a);
}