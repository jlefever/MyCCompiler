int main()
{
    printf("0 || 0 is %d\n", 0 || 0);
    printf("1 || 0 is %d\n", 1 || 0);
    printf("0 || 1 is %d\n", 0 || 1);
    printf("1 || 1 is %d\n", 1 || 1);
    printf("0 && 0 is %d\n", 0 && 0);
    printf("1 && 0 is %d\n", 1 && 0);
    printf("0 && 1 is %d\n", 0 && 1);
    printf("1 && 1 is %d\n", 1 && 1);
}